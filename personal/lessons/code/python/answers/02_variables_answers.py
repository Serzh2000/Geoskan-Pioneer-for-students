# Ответы к задачам главы "Переменные и типы данных"

# Задача 1. Целое число
# Создайте переменную `speed` и присвойте ей значение 5. Выведите её.
speed = 5
print(speed)

# Задача 2. Дробное число
# Создайте переменную `height` и присвойте ей значение 1.5. Выведите её.
height = 1.5
print(height)

# Задача 3. Строка
# Создайте переменную `name` со значением "Pioneer". Выведите её.
name = "Pioneer"
print(name)

# Задача 4. Изменение значения
# Создайте переменную `score` = 10. Затем измените её значение на 20. Выведите `score`.
score = 10
score = 20
print(score)

# Задача 5. Изменение типа
# Создайте переменную `x` = 100. Затем присвойте ей строку "Сто". Выведите тип переменной до и после.
x = 100
print(type(x))
x = "Сто"
print(type(x))

# Задача 6. Сумма переменных
# Даны `a = 5` и `b = 7`. Создайте переменную `c`, равную их сумме. Выведите `c`.
a = 5
b = 7
c = a + b
print(c)

# Задача 7. Склейка строк
# Даны `part1 = "Geo"` и `part2 = "skan"`. Создайте `full`, сложив их.
part1 = "Geo"
part2 = "skan"
full = part1 + part2
print(full)

# Задача 8. Дублирование строки
# Создайте строку `s = "Fly"`. Выведите её 3 раза подряд (используйте умножение).
s = "Fly"
print(s * 3)

# Задача 9. Проверка типа
# Какого типа переменная `pi = 3.1415`? Выведите результат функции `type(pi)`.
pi = 3.1415
print(type(pi))

# Задача 10. Множественное присваивание
# Присвойте переменным `x`, `y`, `z` значения 1, 2, 3 в одной строке.
x, y, z = 1, 2, 3
print(x, y, z)

# Задача 11. Обмен значениями
# Даны `a = 10`, `b = 20`. Поменяйте их значения местами без третьей переменной.
a = 10
b = 20
a, b = b, a
print(a, b) # Должно быть 20 10

# Задача 12. Логическая переменная
# Создайте переменную `is_flying` со значением True. Выведите её.
is_flying = True
print(is_flying)

# Задача 13. Преобразование в float
# Дано целое число `n = 5`. Преобразуйте его в дробное и сохраните в `f`.
n = 5
f = float(n)
print(f) # 5.0

# Задача 14. Строка в число
# Дана строка `s_num = "123"`. Преобразуйте её в число `num` и прибавьте 1.
s_num = "123"
num = int(s_num)
print(num + 1)

# Задача 15. Вычисление
# Даны `mass = 10` и `accel = 9.8`. Вычислите `force = mass * accel`.
mass = 10
accel = 9.8
force = mass * accel
print(force)
