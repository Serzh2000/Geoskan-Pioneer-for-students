# Ответы к задачам главы "Циклы"

# Задача 1. От 0 до 4
# Выведите числа от 0 до 4 используя `range(5)`.
for i in range(5):
    print(i)

# Задача 2. Приветствие
# Выведите слово "Привет" 3 раза.
for i in range(3):
    print("Привет")

# Задача 3. От 1 до 5
# Выведите числа от 1 до 5 (используйте `range(1, 6)`).
for i in range(1, 6):
    print(i)

# Задача 4. Четные числа
# Выведите четные числа от 0 до 8 (`range(0, 9, 2)`).
for i in range(0, 9, 2):
    print(i)

# Задача 5. Сумма чисел
# Найдите сумму чисел от 1 до 100.
total = 0
for i in range(1, 101):
    total += i
print(total)

# Задача 6. Обратный отсчет
# Выведите числа от 5 до 1 (`range(5, 0, -1)`).
for i in range(5, 0, -1):
    print(i)

# Задача 7. Таблица умножения
# Выведите таблицу умножения на 5 (от 1*5 до 10*5).
for i in range(1, 11):
    print(i * 5)

# Задача 8. Посимвольный вывод
# Дана строка "Дрон". Выведите каждую букву с новой строки.
s = "Дрон"
for char in s:
    print(char)

# Задача 9. Цикл while
# Выводите `x`, пока `x < 3`. Начальное `x = 0`.
x = 0
while x < 3:
    print(x)
    x += 1

# Задача 10. Прерывание цикла
# Ищите число 7 в диапазоне от 0 до 10. Если нашли — `break`.
for i in range(11):
    if i == 7:
        print("Нашел 7!")
        break

# Задача 11. Пропуск итерации
# Выведите числа от 0 до 4, пропустив 2 (`continue`).
for i in range(5):
    if i == 2:
        continue
    print(i)

# Задача 12. Квадраты чисел
# Выведите квадраты чисел от 1 до 5.
for i in range(1, 6):
    print(i ** 2)

# Задача 13. Длина строки без len()
# Посчитайте длину строки "Hello" с помощью цикла.
s = "Hello"
count = 0
for char in s:
    count += 1
print(count)

# Задача 14. Рисуем звезды
# Выведите 5 строк, в каждой по одной звездочке `*`.
for i in range(5):
    print("*")

# Задача 15. Лесенка
# Выведите лесенку из звезд (1, 2, 3...).
for i in range(1, 4):
    print("*" * i)
