# Ответы к задачам главы "Логические операции"

# Задача 1. Больше
# Проверьте, больше ли 10 числа 5.
print(10 > 5)

# Задача 2. Меньше или равно
# Проверьте, меньше или равно 5 числу 5.
print(5 <= 5)

# Задача 3. Равенство
# Равны ли числа 100 и 100?
print(100 == 100)

# Задача 4. Неравенство
# Правда ли, что 10 не равно 20?
print(10 != 20)

# Задача 5. Логическое И (AND)
# Проверьте: (5 > 0) и (5 < 10).
print(5 > 0 and 5 < 10)

# Задача 6. Логическое ИЛИ (OR)
# Проверьте: (5 > 0) или (5 > 10).
print(5 > 0 or 5 > 10)

# Задача 7. Логическое НЕ (NOT)
# Инвертируйте значение True.
print(not True)

# Задача 8. Попадание в диапазон
# Дано `x = 15`. Проверьте, лежит ли `x` между 10 и 20 (строго).
x = 15
print(x > 10 and x < 20)

# Задача 9. Проверка на четность
# Дано `n = 4`. Проверьте, делится ли оно на 2 без остатка (`n % 2 == 0`).
n = 4
print(n % 2 == 0)

# Задача 10. Сравнение строк
# Равны ли строки "test" и "Test"?
print("test" == "Test") # False

# Задача 11. Полет разрешен?
# Дано `battery = 4.0` и `connected = True`. Разрешен ли полет? (нужно и то, и другое).
battery = 4.0
connected = True
print(battery > 3.5 and connected)

# Задача 12. Опасность
# Дано `obstacle = False`. Есть ли опасность? (Если препятствие есть ИЛИ высота < 0.5).
obstacle = False
height = 1.0
print(obstacle or height < 0.5)

# Задача 13. Сложное условие
# Проверьте: (2 + 2 == 4) и (не (3 > 5)).
print((2 + 2 == 4) and (not (3 > 5)))

# Задача 14. Сравнение разных типов
# Равны ли число 5 и строка "5"?
print(5 == "5") # False

# Задача 15. Истина или Ложь
# Чему равно `bool(1)`? А `bool(0)`?
print(bool(1)) # True
print(bool(0)) # False
