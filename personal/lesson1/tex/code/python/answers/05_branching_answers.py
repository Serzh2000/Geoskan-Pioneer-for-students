# Ответы к задачам главы "Ветвление"

# Задача 1. Положительное число
# Дано число `x = 10`. Если оно больше 0, выведите "Положительное".
x = 10
if x > 0:
    print("Положительное")

# Задача 2. Четное или нечетное
# Дано `n = 7`. Если делится на 2 без остатка — "Четное", иначе — "Нечетное".
n = 7
if n % 2 == 0:
    print("Четное")
else:
    print("Нечетное")

# Задача 3. Совершеннолетие
# Дано `age = 15`. Если `age >= 18`, выведите "Взрослый", иначе "Ребенок".
age = 15
if age >= 18:
    print("Взрослый")
else:
    print("Ребенок")

# Задача 4. Пароль
# Дано `password = "1234"`. Если пароль совпадает с "secret", пишем "Доступ разрешен", иначе "Отказ".
password = "1234"
if password == "secret":
    print("Доступ разрешен")
else:
    print("Отказ")

# Задача 5. Сравнение чисел
# Даны `a = 10`, `b = 20`. Выведите большее из них.
a = 10
b = 20
if a > b:
    print(a)
else:
    print(b)

# Задача 6. Знак числа (elif)
# Дано `x = 0`. Выведите "Плюс", "Минус" или "Ноль".
x = 0
if x > 0:
    print("Плюс")
elif x < 0:
    print("Минус")
else:
    print("Ноль")

# Задача 7. Светофор
# Дано `color = "red"`. Если "green" — "Ехать", "yellow" — "Ждать", "red" — "Стоять".
color = "red"
if color == "green":
    print("Ехать")
elif color == "yellow":
    print("Ждать")
elif color == "red":
    print("Стоять")
else:
    print("Сломан")

# Задача 8. Батарея
# `voltage = 3.6`. Если < 3.5 — "Посадка!", иначе "Полет нормальный".
voltage = 3.6
if voltage < 3.5:
    print("Посадка!")
else:
    print("Полет нормальный")

# Задача 9. Делимость
# Делится ли число 15 на 3 и на 5 одновременно?
n = 15
if n % 3 == 0 and n % 5 == 0:
    print("Делится")
else:
    print("Не делится")

# Задача 10. Длина имени
# Дано `name = "Alex"`. Если длина имени > 5 букв, напишите "Длинное", иначе "Короткое". (функция len())
name = "Alex"
if len(name) > 5:
    print("Длинное")
else:
    print("Короткое")

# Задача 11. Пустая строка
# Дана строка `s = ""`. Если она пустая (длина 0), напишите "Пусто".
s = ""
if len(s) == 0:
    print("Пусто")

# Задача 12. Категория веса
# `weight = 80`. < 50 — "Легкий", 50-100 — "Средний", > 100 — "Тяжелый".
weight = 80
if weight < 50:
    print("Легкий")
elif weight <= 100:
    print("Средний")
else:
    print("Тяжелый")

# Задача 13. Доступ к полету
# `armed = True`, `ready = False`. Если оба True — "Взлет", иначе "Ждать".
armed = True
ready = False
if armed and ready:
    print("Взлет")
else:
    print("Ждать")

# Задача 14. Логин
# `login = "admin"`. Если не равен "admin", вывести "Ошибка".
login = "user"
if login != "admin":
    print("Ошибка")

# Задача 15. Максимум из трех
# `a=5, b=2, c=8`. Найдите самое большое число через if.
a, b, c = 5, 2, 8
m = a
if b > m:
    m = b
if c > m:
    m = c
print(m)
